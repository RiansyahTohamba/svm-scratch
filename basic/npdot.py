import numpy as np 
a = np.arange(3)
b = np.arange(3)
print(a)
print(b)
print(np.dot(a,b))

# In data science and machine learning, the dot product is often used to measure the 'similarity' or correspondence between two feature vectors. The resulting scalar value indicates how similar the two vectors are.

# DOT product use case in several domain
#- **Computer Vision**: The dot product can be used in operations like the calculation of gradients for edge detection.
# - **NLP**: The dot product is fundamental in calculating similarity scores between word vectors or in attention mechanisms.
# - **Audio Processing**: The dot product can be used in calculating correlations or similarities between different audio segments.
